package Java_IV_b.Dakar;

public class Vehiculo {
    int velocidad;
    double aceleracion;
    double anguloDeGiro;
    String patente;
    int peso;
    int ruedas;

    public Vehiculo(int velocidad, double aceleracion, double anguloDeGiro, String patente, int peso, int ruedas) {
        this.velocidad = velocidad;
        this.aceleracion = aceleracion;
        this.anguloDeGiro = anguloDeGiro;
        this.patente = patente;
        this.peso = peso;
        this.ruedas = ruedas;
    }
}
